package com.diwa.pattren;

public class RightTrianglePattern {
	public static void main(String[] args) {
		 	int i,j,row=6; // i for rows and j for columns and row denotes the number of rows you want to print 
		 	for(i=0;i<row; i++) { //outer loop for rows
		 		for (j=0;j<=i;j++) {
		 			System.out.print("* ");
		 		}
		 		System.out.println();
		 		
		 	}
		 	
	}

}
